# Notion de projet
    Un projet est un ensemble coordonnées de travaux sous contraites de coûts délimités dans le temps et qui a pour objectif la satisfaction des besoins des utilisateurs de l'information.

    Dans le domaine des systèmes d'information on distingue deux types de projets:
    1. Le projet du domaine des technologies informatiques (mise en place des ordinateurs, des réseaux, des équipements de sécurités, etc).

    2. Projet système d'information qui modifie du comportement du système d'information et s'inscrit dans un contexte globale (stratégie, fusion, réorganisation...).

    Ce type de projet touche une partie déterminée du système d'information (le périmètre fonctionel du projet) généralement un projet d'application des technologies informatiques ou projet d'informatisation.
    Plus généralement, un projet informatique (qui concerne le système informatique) peut se ratacher aux catégories suivantes :
    - Projet d'amélioration de l'existant : retouche, évolution logiciel, renfocement des capacités de traitement sans modifié l'architecture applicative.
    - Projet de développement : ajout d'une nouvelle fonctionnalitée du système d'information ou remplacement d'une fonction existante.


